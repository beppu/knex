module.exports = function(client) {

  client.initSchema();

  var SchemaBuilder = client.SchemaBuilder;
  var _ = require('lodash');
  var equal = require('assert').equal;
  var deepEqual = require('assert').deepEqual;

  describe("SQLite SchemaBuilder", function() {

    it("basic create table", function() {
      var tableSql = new SchemaBuilder().createTable('users', function(table) {
        table.increments('id');
        table.string('email');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('create table "users" ("id" integer not null primary key autoincrement, "email" varchar(255))');
    });

    it("basic alter table", function() {
      var tableSql = new SchemaBuilder().alterTable('users', function(table) {
        table.increments('id');
        table.string('email');
      }).toSql();

      equal(2, tableSql.length);
      var expected = [
        'alter table "users" add column "id" integer not null primary key autoincrement',
        'alter table "users" add column "email" varchar(255)',
      ];
      expect(expected).to.eql(_.pluck(tableSql, 'sql'));
    });

    it("drop table", function() {
      var tableSql = new SchemaBuilder().dropTable('users').toSql();
      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('drop table "users"');
    });

    it("drop table if exists", function() {
      var tableSql = new SchemaBuilder().dropTableIfExists('users').toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('drop table if exists "users"');
    });

    it("drop unique", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.dropUnique('foo');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('drop index foo');
    });

    it("drop index", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.dropIndex('foo');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('drop index foo');
    });

    it("rename table", function() {
      var tableSql = new SchemaBuilder().renameTable('users', 'foo').toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" rename to "foo"');
    });

    it("adding primary key", function() {
      var tableSql = new SchemaBuilder().createTable('users', function(table) {
        table.string('foo').primary();
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('create table "users" ("foo" varchar(255), primary key ("foo"))');
    });

    it("adding foreign key", function() {
      var tableSql = new SchemaBuilder().createTable('users', function(table) {
        table.string('foo').primary();
        table.string('order_id');
        table.foreign('order_id').references('id').on('orders');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('create table "users" ("foo" varchar(255), "order_id" varchar(255), foreign key("order_id") references "orders"("id"), primary key ("foo"))');
    });

    it("adds a unique key with autogenerated name", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.unique('foo');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('create unique index users_foo_unique on "users" ("foo")');
    });

    it("adding unique key with specific name", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.unique('foo', 'bar');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('create unique index bar on "users" ("foo")');
    });

    it("adding index", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.index(['foo', 'bar'], 'baz');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('create index baz on "users" ("foo", "bar")');
    });

    it("adding incrementing id", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.increments('id');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "id" integer not null primary key autoincrement');
    });

    it("adding big incrementing id", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.bigIncrements('id');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "id" integer not null primary key autoincrement');
    });

    it("adding string", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.string('foo');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "foo" varchar(255)');
    });

    it("allows setting a value in the string length, although unused by sqlite3", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.string('foo', 100);
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "foo" varchar(100)');
    });

    it("chains notNull and defaultTo", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.string('foo', 100).notNull().defaultTo('bar');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "foo" varchar(100) not null default \'bar\'');
    });

    it("adding text", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.text('foo');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "foo" text');
    });

    it("adding big integer", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.bigInteger('foo');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "foo" bigint');
    });

    it("bigincrements works the same as increments for sqlite3", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.bigIncrements('foo');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "foo" integer not null primary key autoincrement');
    });

    it("adding integer", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.integer('foo');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "foo" integer');
    });

    it("adding autoincrements", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.increments('foo');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "foo" integer not null primary key autoincrement');
    });

    it("adding medium integer", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.mediumint('foo');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "foo" integer');
    });

    it("adding tiny integer", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.tinyint('foo');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "foo" tinyint(1)');
    });

    it("adding small integer", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.smallint('foo');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "foo" integer');
    });

    it("adding float", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.float('foo', 5, 2);
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "foo" float');
    });

    it("adding double", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.double('foo', 15, 8);
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "foo" float');
    });

    it("adding decimal", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.decimal('foo', 5, 2);
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "foo" float');
    });

    it("adding boolean", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.boolean('foo');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "foo" boolean');
    });

    it("adding enum", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.enum('foo', ['bar', 'baz']);
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "foo" varchar');
    });

    it("adding date", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.date('foo');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "foo" date');
    });

    it("adding date time", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.dateTime('foo');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "foo" datetime');
    });

    it("adding time", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.time('foo');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "foo" time');
    });

    it("adding time stamp", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.timestamp('foo');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "foo" datetime');
    });

    it("adding time stamps", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.timestamps();
      }).toSql();

      equal(2, tableSql.length);
      var expected = [
        'alter table "users" add column "created_at" datetime',
        'alter table "users" add column "updated_at" datetime'
      ];
      deepEqual(expected, _.pluck(tableSql, 'sql'));
    });

    it("adding binary", function() {
      var tableSql = new SchemaBuilder().table('users', function(table) {
        table.binary('foo');
      }).toSql();

      equal(1, tableSql.length);
      expect(tableSql[0].sql).to.equal('alter table "users" add column "foo" blob');
    });

  });
};